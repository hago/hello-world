package com.hagoapp.poc.sbweb.controller;

public class AppInfo {
    private String id;
    private String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public static AppInfo create(String id, String name) {
        var a = new AppInfo();
        a.setId(id);
        a.setName(name);
        return a;
    }
}
